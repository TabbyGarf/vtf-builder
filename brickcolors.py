for match in pattern.finditer(raw_data):
    name = match.group(1).strip()
    r = int(match.group(2))
    g = int(match.group(3))
    b = int(match.group(4))
    a = 255
    colors.append({
        "name": name,
        "rgba": [r, g, b, a]
    })

